#!/usr/bin/env bash
set -euo pipefail

experiment_root="$(cd "$(dirname "$0")" && pwd -P)"
repo_root="$(cd "$experiment_root/../.." && pwd -P)"
public_files=(
  "$repo_root/docs/deliberation-experiment.md"
  "$experiment_root/README.md"
  "$experiment_root/scored-comparison.md"
  "$experiment_root/issue-442-results-comment.md"
)
while IFS= read -r case_file; do
  public_files+=("$case_file")
done < <(find "$experiment_root/cases" -type f -name '*.md' | sort)

status=0
check() {
  local description="$1"
  local pattern="$2"
  shift 2
  if rg -n --pcre2 "$pattern" "$@"; then
    printf 'public artifact check failed: %s\n' "$description" >&2
    status=1
  fi
}

check "absolute home path" \
  '(/home/[A-Za-z0-9._-]+/|/Users/[A-Za-z0-9._-]+/|[A-Za-z]:\\\\Users\\\\)' \
  "$experiment_root" "$repo_root/docs/deliberation-experiment.md"
check "private address or local service" \
  '(\b10\.(?:[0-9]{1,3}\.){2}[0-9]{1,3}\b|\b172\.(?:1[6-9]|2[0-9]|3[01])\.(?:[0-9]{1,3}\.)[0-9]{1,3}\b|\b192\.168\.(?:[0-9]{1,3}\.)[0-9]{1,3}\b|localhost:[0-9]+)' \
  "${public_files[@]}"
check "em dash" '—' "${public_files[@]}"
check "authorship disclosure" \
  '(?i)(written with help from|generated by|co-written by|drafted by|assisted by|generated with|co-authored-by)' \
  "${public_files[@]}"
check "credential-shaped text" \
  '(sk-[A-Za-z0-9_-]{20,}|gh[pousr]_[A-Za-z0-9]{20,}|AKIA[0-9A-Z]{16}|Bearer [A-Za-z0-9._~-]{20,})' \
  "$experiment_root"

exit "$status"
